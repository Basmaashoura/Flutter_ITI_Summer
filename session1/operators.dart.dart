void main() {
    num first = 5;
    num second = 7.25;

    print(first + second);
    print(first - second);
    print(first * second);
    print(first / second);
    print(first % second);

    // ================ //

    print(first > second);
    print(first < second);
    print(first == second);

    // ================ //

    print(first > 0 && second > 0);
}